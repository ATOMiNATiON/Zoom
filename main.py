import csv

###############################################################################
# Functions
###############################################################################
def dic(lst):
    d = dict()
    for item in lst:
        if item[0] not in d:
            d[item[0]] = item[1:]

    return d
# end dic()

def read(file):
    with open(file, "r") as f:
        l = csv.reader(f)
        lst = [row for row in l]

    return lst
# end read()
###############################################################################
# Main
###############################################################################
def main():
    try:
        x = input("File?: ")
        l = read(x)
        print(dic(l))
    except:
        print("Seems like that file doesn't exist in your directory!")


###############################################################################
# Conditional
###############################################################################
if __name__ == "__main__":
    main()


